int	ft_str_is_alpha(char *str);

int	ft_str_is_alpha(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'z')
		{
			if (!(str[i] > 'Z' && str[i] < 'a'))
				count++;
		}
		i++;
	}
	if (count == i)
		return (1);
	return (0);
}
